#################################################
# CODE FOR CALCULATING THE AREA OF A TRAPEZOID
#################################################

# Formula is 1/2 * (a + b) * h

# where a and b = bases of the parallel sides and h = height of the trapezoid

a = int(input("Enter the value for a = "))

b = int(input("Enter the value of b = "))

h = int(input("Enter the value of h = "))

areaOfTrapezoid = 1/2 * (a + b) * h

print(areaOfTrapezoid)